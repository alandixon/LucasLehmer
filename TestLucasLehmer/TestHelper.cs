using System;
using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace TestLucasLehmer
{
    [TestClass]
    public class TestHelper
    {
        [DataRow("0", "", "", "0000")]
        [DataRow("7", "111", "111", "0111")]
        [DataRow("8", "1000", "1000", "1000")]
        [DataRow("F", "1111", "1111", "1111")]
        [DataRow("01", "0001", "0001", "00000001")]
        [DataRow("0F", "1111", "1111", "00001111")]
        [DataRow("67", "1100111", "1100111", "01100111")]
        [DataRow("76", "1110110", "1110110", "01110110")]
        [DataRow("E3", "11100011", "11100011", "11100011")]
        [DataRow("FF", "11111111", "11111111", "11111111")]
        [DataRow("1FFFFFFFFFFFFFFE", "1111111111111111111111111111111111111111111111111111111111110", "1111111111111111111111111111111111111111111111111111111111110", "0001111111111111111111111111111111111111111111111111111111111110")]
        [DataRow("FFFFFFFEFFFFFFFF", "1111111111111111111111111111111011111111111111111111111111111111", "1111111111111111111111111111111011111111111111111111111111111111", "1111111111111111111111111111111011111111111111111111111111111111")]
        [DataRow("FFFFFFFFFFFFFFFE", "1111111111111111111111111111111111111111111111111111111111111110", "1111111111111111111111111111111111111111111111111111111111111110", "1111111111111111111111111111111111111111111111111111111111111110")]
        [DataRow("FFFFFFFFFFFFFFFF", "1111111111111111111111111111111111111111111111111111111111111111", "1111111111111111111111111111111111111111111111111111111111111111", "1111111111111111111111111111111111111111111111111111111111111111")]
        [DataTestMethod]
        public void TestHex2Bin(string hex, string binWithImplicitNoLeadingZeros, string binWithExplicitNoLeadingZeros, string binWithExplicitLeadingZeros)
        {
            Assert.AreEqual(binWithImplicitNoLeadingZeros, LucasLehmer.Helper.Hex2Bin(hex));
            Assert.AreEqual(binWithExplicitNoLeadingZeros, LucasLehmer.Helper.Hex2Bin(hex, false));
            Assert.AreEqual(binWithExplicitLeadingZeros, LucasLehmer.Helper.Hex2Bin(hex, true));
        }

    }
}
